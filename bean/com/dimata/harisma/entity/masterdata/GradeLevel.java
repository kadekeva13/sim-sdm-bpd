package com.dimata.harisma.entity.masterdata;

import com.dimata.qdep.entity.Entity;

/**
 *
 * @author Satrya Ramayu
 */
public class GradeLevel extends  Entity{
    private String codeLevel;
    private int gradeRank;
    
     public GradeLevel(){
        
    }

    public GradeLevel(long oid,String codeLevel, int gradeRank ){
        this.setOID(oid);
        this.codeLevel =codeLevel;
        this.gradeRank = gradeRank;
    }
    
    /**
     * @return the codeLevel
     */
    public String getCodeLevel() {
        if(codeLevel==null){
            return "";
        }
        return codeLevel;
    }

    /**
     * @param codeLevel the codeLevel to set
     */
    public void setCodeLevel(String codeLevel) {
        this.codeLevel = codeLevel;
    }

    /**
     * @return the gradeRank
     */
    public int getGradeRank() {
        return gradeRank;
    }

    /**
     * @param gradeRank the gradeRank to set
     */
    public void setGradeRank(int gradeRank) {
        this.gradeRank = gradeRank;
    }
}
